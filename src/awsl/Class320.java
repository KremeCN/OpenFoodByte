package awsl;

public class Class320 extends Class326 {
   public static final double Method1010(double a, double a, double a, double a) {
      return a == 0.0 ? a : a * Math.pow(2.0, 10.0 * (a / a - 1.0)) + a;
   }

   public static final double Method1012(double a, double a, double a, double a) {
      return a == a ? a + a : a * (-Math.pow(2.0, -10.0 * a / a) + 1.0) + a;
   }

   public static final double Method1014(double a, double a, double a, double a) {
      if (a == 0.0) {
         return a;
      } else if (a == a) {
         return a + a;
      } else {
         return (a /= a / 2.0) < 1.0 ? a / 2.0 * Math.pow(2.0, 10.0 * (a - 1.0)) + a : a / 2.0 * (-Math.pow(2.0, -10.0 * --a) + 2.0) + a;
      }
   }
}
